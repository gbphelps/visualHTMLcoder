import { create, format } from './utils'
import { draggerCallback } from './listeners/draggerCallback'

export const initializeDragger = (d1diameter) => {
  const dragger =
    create('DIV', slider, {
      id: 'dragger',
      status: 0,
      diameter: d1diameter
    },{
      height: d1diameter + 'px',
      width: d1diameter + 'px',
      'border-radius': '50%',
      top: -d1diameter/2 + 'px',
      left: -(d1diameter-slider.width)/2 + 'px',
      position: 'absolute',
      background: format(canvas.color)
    });
  dragger.addEventListener('mousedown', draggerCallback);
}
